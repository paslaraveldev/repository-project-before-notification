function showLevelDropdown() {
        // Show Alevel Dropdown
        $('#alevelDropdown').show();
        // Hide other dropdowns
        $('#artsDropdown, #scienceDropdown').hide();
    }

    function showArtsDropdown() {
        // Show Arts Dropdown
        $('#artsDropdown').show();
        // Hide other dropdowns
        $('#scienceDropdown').hide();
    }

    function showScienceDropdown() {
        // Show Sciences Dropdown
        $('#scienceDropdown').show();
        // Hide other dropdowns
        $('#artsDropdown').hide();
    }